#String indices

string = "Python Tutorial"
print(string)

print("Print first character from the left: ", string[0])
print("Print last character from the left: ", string[14])
print("\n")
print("Print first character from the right: ", string[-1])
print("\n")
print("Print 'n' letter from the left: ", string[5])
print("Print 'n' letter from the right: ", string[-10])
print("Print 'Tutor' from the left:", string[7:12])
print("Print 'Tutor' from the right:", string[-8:-3])
print("Print 'hon' from the left:", string[3:6])
print("Print 'hon' from the right:", string[-12:-9])
print("\n")
print("z" in string)
print("a" in string)
print("on" in string)
print("PT" in string)
