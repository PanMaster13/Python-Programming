#Simple for loop
print("Increase loop with 1 parameter")
for a in range(10):
    print(a)
    
print("\nIncrease loop with 2 parameter")
for a in range(12,21):
    print(a)

print("\nIncrease loop with 3 parameter")
for a in range(12,21,3):
    print(a)

print("\nDecrease loop with 3 parameter")
for a in range(21,10,-3):
    print(a)

print("\nDecrease loop with 3 parameter")
for a in range(21,16,-1):
    print(a)
